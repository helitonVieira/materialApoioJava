Intellyj ()
  ctrol + O (ver as propriedades da classe)
  ctrol + shift + F10 (start projeto)
  Alt + 1 (vai para inicio da arvore , fecha project ,abre o project)
  Alt + insert (Opção para criar algo [ex:classe, metodo, variavel get set])
  Ctrol + Alt + O (organizar os imports)
  Ctrol + f4 (Fechar a classe)

PESQUISA
  Ctrol + N (Pesquisar classe)
  Ctrol + Shift + N (Qualquer arquivo)
  Ctrol + Shift + A (Pesquisa tudo sobre o intellyJ)

MOVIMENTAR ENTRE TEXTO E ARQUIVOS
  Ctrol + B (ir para local esta a variavel)
  Ctrol + Alt + seta (mover cursor onde estava antes e depois)

MANIPULAR TEXTO
  ctrol + D (Duplicar a linha)
  Ctrol + Alt + L (identar o codigo)
